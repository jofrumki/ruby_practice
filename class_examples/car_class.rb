class Vehicle
    def initialize (make, model, year, passengers, mpg, tech)
        @make, @model, @year, @passengers, @mpg, @tech = make, model, year, passengers, mpg, tech
    end 
    
    def make
        @make
    end
    
    def model
        @model
    end
    
    def year
        @year
    end
    
    def riders
        @passengers
    end
    
    def mpg
        @mpg
    end
    
    def package
        @tech
    end
end 
    
    
    